            let uploadedRecords = customerRecords.filter { $0.isUploaded }
            let pendingRecords = customerRecords.filter { !$0.isUploaded }
            
            if uploadedRecords.isEmpty {
                await MainActor.run {
                    showError("Bu m√º≈üteriye ait y√ºklenmi≈ü resim bulunamadƒ±. Sadece bekleyen resimler var.")
                }
                return
            }
            
            // Y√ºklenmi≈ü resimlerin ID'lerini topla
            let uploadedIds = uploadedRecords.map { $0.id }
            let uploadedPaths = uploadedRecords.map { $0.resimYolu }
            
            // 1Ô∏è‚É£ Database'den sadece y√ºklenmi≈ü resimleri sil
            let dbDeleteSuccess = dbManager.deleteImagesByIds(uploadedIds)
            
            // 2Ô∏è‚É£ Sadece y√ºklenmi≈ü resim dosyalarƒ±nƒ± sil
            var fileDeleteSuccess = true
            var deletedFileCount = 0
            
            for imagePath in uploadedPaths {
                let success = await ImageStorageManager.deleteImage(at: imagePath)
                if success {
                    deletedFileCount += 1
                } else {
                    fileDeleteSuccess = false
                }
            }
            
            await MainActor.run {
                if dbDeleteSuccess || deletedFileCount > 0 {
                    // UI'dan sadece y√ºklenmi≈ü resimleri kaldƒ±r
                    savedImages.removeAll { image in
                        uploadedIds.contains(image.databaseId)
                    }
                    
                    // Ba≈üarƒ± mesajƒ±
                    let message = """
                    ‚úÖ Toplu silme tamamlandƒ±:
                    ‚Ä¢ Silinen: \(uploadedRecords.count) y√ºklenmi≈ü resim
                    ‚Ä¢ Korunan: \(pendingRecords.count) bekleyen resim
                    """
                    
                    // M√º≈üteri gruplarƒ±nƒ± g√ºncelle
                    loadCustomerImageGroups()
                    
                    // Bilgi mesajƒ± (hata deƒüil, bilgilendirme)
                    if pendingRecords.count > 0 {
                        showError(message) // showError yerine showInfo olabilir ama ≈üimdilik bu
                    }
                } else {
                    // Hi√ßbir silme i≈ülemi ba≈üarƒ±sƒ±z
                    showError("Y√ºklenmi≈ü resimler silinemedi")
                }
            }
        }
    }
    
